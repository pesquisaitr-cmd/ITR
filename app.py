# Função para extrair dados formatados do PDF
def extrair_dados_pdf_formatado(caminho_pdf):   # VTN: Lavoura-AB, AR, AR, Pastagem,
    with pdfplumber.open(caminho_pdf) as pdf:   # Silvicultura, Preservação. 20 estados;  2.776 Municípios
        dados = []

        # Percorrer todas as páginas do PDF
        for pagina in pdf.pages:
            tabelas = pagina.extract_tables()

            # Percorrer todas as tabelas encontradas
            for tabela in tabelas:
                for linha in tabela:
                    # Ignorar cabeçalhos e linhas inválidas
                    if not linha[0] or 'UF' in linha[0] or 'Nome Município' in linha[0] or "VALORES DE TERRA" in linha[0]:
                        continue

                    # Extrair UF e município
                    uf = linha[0].strip().upper() if linha[0] else None
                    municipio = normalizar_texto(linha[1]) if linha[1] else None

                    # Criar chave de busca para o mapeamento
                    chave_busca = f"{municipio} - {uf}" if municipio and uf else municipio

                    # Corrigir nome do município usando o mapeamento
                    municipio_corrigido = mapeamento_municipios.get(chave_busca, municipio)

                    # Extrair valores das colunas
                    lavoura_boa = linha[2] if linha[2] else None
                    lavoura_regular = linha[3] if linha[3] else None
                    lavoura_restrita = linha[4] if linha[4] else None
                    pastagem_plantada = linha[5] if linha[5] else None
                    silvicultura = linha[6] if linha[6] else None
                    fauna_flora = linha[7] if linha[7] else None

                    # Adicionar linha formatada à lista de dados
                    dados.append([
                        uf, municipio_corrigido, lavoura_boa, lavoura_regular, lavoura_restrita,
                        pastagem_plantada, silvicultura, fauna_flora
                    ])

 # Criar DataFrame com os dados extraídos
    colunas = [
        'UF', 'Nome Município', 'Lavoura Aptidão Boa', 'Lavoura Aptidão Regular',
        'Lavoura Aptidão Restrita', 'Pastagem Plantada',
        'Silvicultura ou Pastagem Natural', 'Preservação da Fauna e da Flora'
    ]
    df = pd.DataFrame(dados, columns=colunas)

    # Limpeza dos dados
    df = df.dropna(how='all')                      # remover linhas totalmente vazias
    df = df.dropna(subset=['Nome Município'])      # remover linhas sem município
    df = df.drop_duplicates(subset=['UF', 'Nome Município'])  # remover duplicatas

    # Função para converter valores monetários para float
    def converter_para_float(valor):
        if isinstance(valor, str):
            valor = valor.replace("R$", "").replace(".", "").replace(",", ".").strip()
            try:
                return float(valor)
            except ValueError:
                return None
        return None

    # Aplicar conversão nas colunas de valores
    colunas_valores = [
        'Lavoura Aptidão Boa', 'Lavoura Aptidão Regular', 'Lavoura Aptidão Restrita',
        'Pastagem Plantada', 'Silvicultura ou Pastagem Natural', 'Preservação da Fauna e da Flora'
    ]
    for coluna in colunas_valores:
        df[coluna] = df[coluna].apply(converter_para_float)

    return df

