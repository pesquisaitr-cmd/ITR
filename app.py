    AT = row["AT IMÓVEL"]
    GU_FIXO = row["GU_FIXO"]

    for limite_area, gu_faixas in faixas:
        if AT <= limite_area:
            for limite_gu, aliquota1 in gu_faixas:
                if GU_FIXO <= limite_gu:
                    return aliquota1
    return None  # caso não encontre nenhuma faixa

